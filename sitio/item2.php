                    <h2 id="uno">Sindicato Argentino</h2>
                    <p>"El Sindicato Argentino del Hip-Hop se formó a principios de los 90´s, con el propósito de difundir el Hip Hop en Argentina, elevando al mismo tiempo el conocimiento de dicha cultura. Años más tarde en 1992, 12 jóvenes se arman de instrumentos en vivo Smoler voz, Derek Dogg voz , Huexo vos, fabri Batería, el chino Bajo. Marin guitarra, Alejandro Guitarra 2 El cubano, teclados y programaciones. El pipi percusión para concretar su plan de desarrollar en su país un género hasta entonces no reconocido. En 1997, El Sindicato graba "Del Barrio y Pide Más" para el primer compilado del Hip Hop Argentino titulado "Nación Hip Hop". Del Barrio es el primer video del grupo, y el que al mismo tiempo lo proyecta internacionalmente. Al año siguiente, hacen giras por el interior del país, y en algunos otros países de América Latina, de esa manera consiguen el éxito under del grupo. Ese mismo año se edita MTV Lingo, el compilado que reúne al Sindicato Argentino del Hip Hop, Cypress Hill, Control Machete, Illya Kuryaki y Delinquent Habits, entre otros actos de renombre del Hip Hop.
                        <br>En 1999, después de un receso, la vieja escuela del Oeste vuelve a sus raíces, esta vez con 4 músicos-raperos: Smoler, Frost, Derek, Huexo y el DJ Fabry.
                        <br>En el 2000, El Sindicato, después de una década de lucha y supervivencia, con varios cambios, firma el contrato con Universal y graba el álbum Un Paso A La Eternidad, con el que se proponen trascender las fronteras del público exclusivamente Hip Hopero, tratando al mismo tiempo de no perder la esencia del género y aprovechando al máximo las posibilidades rítmicas y sonoras del rap en español.
                            El álbum contiene 14 temas y la producción artística estuvo a cargo de Cachorro López, ex Abuelos de la Nada, y Juan Blas Caballero. Los invitados del disco son: Diego Torres, Alexander Batista y Julieta Ortega, entre otros.
                            El primer sencillo Mil Horas, es, justamente, una ingeniosa versión del clásico de Los Abuelos de la Nada, adaptada y versionada con el sello indiscutible del Sindicato Argentino del Hip Hop.
                            En el 2005 el Sindicato, integrado por Huexo, Smoler y Dj Fabry, viajó a Puerto Rico, donde permanecieron tres meses. Allí compartiendo tarima con Israel Vibration, Julio Voltio, La Secta, DJ Dara, entre otros. En abril realizaron una gira promocional en Miami y en mayo realizaron dos conciertos en México (DF y Toluca).
                            El 29 de marzo de 2005 salió en Estados Unidos y Puerto Rico su segunda producción para Universal/Machete Music, "Sangre, Sudor y Furia". El 21 de abril salió a la venta en Argentina. Esta producción cuenta con la participación de destacados artistas como: Julieta Venegas, DOSHERMANOS, El Chojin, Tego Calderón, Vico C, Loco de la casa Phonky, D-Shon, Full Nelson y T. Weaponz. También cuenta con dos versiones de la canción "Los 6 Fantásticos" junto a Tego Calderón y Vico C, una versión rap y otra versión reggaetón, esta última producida por Luny Tunes.
                    </p>
